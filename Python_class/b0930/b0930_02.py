a1 = 758.12345678
a2 = 25.05
a3 = 150.15

print("1 : ", "%.2f" % a1)
# 소수점, .(온점)도 한자리씩 차지하기에 자릿수 설정 조심
print("2 : ", "%013.2f" % a2)
print("3 : ", "%d %f %s" % (a3, a3, a3))
print("4 : ", "*" * 10)
# 파이썬은 문자와 숫자를 +로 연결시켜 출력할 수 없다. (JS와 문법이 다름)
# 대신 문자끼리면 + 사용 가능, 쉼표(,)와 차이점이라면 쉼표는 한칸 띄어쓰기가 들어감
# 대신 쉼표를 이용하면 둘다 문자열로 인식해 연결시켜 출력한다.
print("-" * 30)
print("%5.1f" % (123456789.123))
# 총 자리수가 오바되더라도 양수 부분은 전부 출력시킨다.
# #10*2=20
# print("%d * %d = %d" % (a,b,a*b))

# 사용 표시 - %s: 문자열, %c: 문자 1개, %f: 실수, %d: 정수
name = "홍길동"
kor = 100
eng = 100
math = 99
# 홍길동 평균: 299, 평균 : 99.6666...
# %f에서 %.nf하면 소수점 n번째자리까지 보여줌. 출력 지정을 안시키면 소수점 6번째 까지는 무조건 보여줌.
print("%s 총합 : %d, 평균 : %.2f" % (name, kor + eng + math, (kor + eng + math) / 3))


