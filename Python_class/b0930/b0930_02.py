print("%05d" % b)

# 001 010 100 3개를 출력하면
num = 1
num2 = 10
num3 = 100
print("%03d" % num)
print("%03d" % num2)
print("%.2f" % num3)
print("%03d %03d %.2f" % (num, num2, num3))
# -기호도 잘 출력됨
